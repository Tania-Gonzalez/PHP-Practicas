<?php 
    //Operadores Incremento y Decremento

        $Anio = 2020;

        //Incremento
        $Anio++;
        echo $Anio.'<br/>';

        //Decremento
        $Anio--;
        echo $Anio.'<br/>';

        //Pre- Incremento
        ++$Anio;
        echo $Anio.'<br/>';
        //Pre- Decremento
        --$Anio;
        echo $Anio.'<br/>';
?>